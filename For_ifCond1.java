package Ex_May26_Loops;

public class For_ifCond1 {
    public static void main(String[] args) {
        for (int i=1; i<=10; i++)
        {
            if(i==5)
            {
                System.out.println("Five");
            }
            else {
                System.out.println(i);
            }
        }
    }
}
