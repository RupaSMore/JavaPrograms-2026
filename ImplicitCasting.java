package Ex_May26;

public class ImplicitCasting {
    public static void main(String[] args) {
        byte b = 20;
        int  c = b;  /// Implicit casting not need to add data type explicitly it happens automatically

        int c1= (int) b;  // Explicit casting, we added data type explicitly


        // Explicit casting
        int d = 200;
        byte e = (byte) d;   //// Explicit Casting
        
    }
}
