package Ex_May26_Loops;

public class For_ifCond2 {
    public static void main(String[] args) {
        for (int i=1; i<=10; i++)
        {
            if(i==5)
            {
                break;
            }

                System.out.println(i);   // 1 to 4 will get printed
        }
    }
}
